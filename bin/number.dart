void main() {
  int number1 = 10; // Deklarasi variabel dengan tipe data int
  double number2 = 3.14; // Deklarasi variabel dengan tipe data double
  num number3 =
      5; // Deklarasi variabel dengan tipe data num (bisa int atau double)

  print('Number 1: $number1'); // Output: Number 1: 10
  print('Number 2: $number2'); // Output: Number 2: 3.14
  print('Number 3: $number3'); // Output: Number 3: 5
}
