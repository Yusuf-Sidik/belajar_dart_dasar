void main() {
  num a = 10;

  a += 10; // Penambahan dan penugasan
  print(a);
  a -= 10; // Pengurangan dan penugasan
  print(a);
  a *= 2; // Perkalian dan penugasan
  print(a);
  a /= 2; // Pembagian dan penugasan
  print(a);
  a ~/= 3; // Pembagian bulat dan penugasan
  print(a);
  a %= 3; // Modulus dan penugasan
  print(a);
}
