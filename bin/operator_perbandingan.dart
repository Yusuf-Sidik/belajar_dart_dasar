void main() {
  print(10 == 10); // true, karena 10 sama dengan 10
  print(10 != 10); // false, karena 10 tidak sama dengan 10
  print(10 > 10); // false, karena 10 tidak lebih besar dari 10
  print(10 < 10); // false, karena 10 tidak lebih kecil dari 10
  print(10 >= 10); // true, karena 10 lebih besar atau sama dengan 10
  print(10 <= 10); // true, karena 10 lebih kecil atau sama dengan 10

  print('yusuf' == 'yusuf'); // true, karena string 'yusuf' sama dengan 'yusuf'
  print('yusuf' !=
      'yusuf'); // false, karena string 'yusuf' tidak berbeda dengan 'yusuf'
}
